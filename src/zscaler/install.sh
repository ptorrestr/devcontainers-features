python_support() {
    PYTHON_BIN=$1
    $PYTHON_BIN -E -s -m pip install --upgrade certifi
    local cafile_path=$($PYTHON_BIN -c 'import ssl; print(ssl.get_default_verify_paths().cafile)')
    local certifi_file_path=$($PYTHON_BIN -c 'import certifi; print(certifi.where())')
    if [[ -f "$certifi_file_path" ]]; then
        rm $certifi_file_path
    fi
    ln -s $cafile_path $certifi_file_path
}

